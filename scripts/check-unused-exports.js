#!/usr/bin/env node
/**
 * Unused exports ratchet.
 *
 * Runs `ts-prune` and compares the count of unused exports against a
 * baseline stored in `scripts/unused-exports-baseline.json`. CI fails if
 * the count grows.
 *
 * Usage:
 *   node scripts/check-unused-exports.js          # check (CI mode)
 *   node scripts/check-unused-exports.js --update # write new baseline
 *
 * Why "count" rather than "list"? The list churns as files move and
 * names change. The count is a stable ratchet â€” each new unused export
 * raises the count, each cleanup lowers it. The intent is monotonic
 * downward pressure without forcing every PR to re-baseline a sorted
 * snapshot.
 *
 * Excludes "(used in module)" entries â€” these are unused-as-public-API
 * but used internally by the file, which is a separate problem class
 * (visibility, not deadness). The ratchet only governs truly dead
 * exports.
 *
 * @module scripts/check-unused-exports
 */

import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)))
const BASELINE_PATH = join(ROOT, 'scripts', 'unused-exports-baseline.json')

/**
 * Path patterns excluded from the unused-exports ratchet.
 *
 * - `src/wasm/mdimension_core/pkg(-validator)?/` â€” Auto-generated by
 *   `wasm-pack build`. Re-emitting the bindings flushes ts-prune
 *   counts; treating these files as "noise" keeps the ratchet
 *   measuring code we author.
 */
const TS_PRUNE_IGNORE = '^src/wasm/mdimension_core/pkg(?:-validator)?(?:/|$)'

/**
 * ts-prune attributes a consumer's import to the original source
 * location, not to a re-exporting barrel. As a result, every line in
 * a barrel that re-exports from another module is reported as
 * "unused" even when the barrel has importers â€” a well-known
 * structural false positive.
 *
 * Filter: if the ts-prune entry points to a line whose form is
 * `export ... from '...'` (or `export * from '...'`), treat the entry
 * as noise and drop it. The original source export is still tracked
 * by ts-prune; this filter only removes the redundant barrel-level
 * report.
 */
const RE_EXPORT_LINE_RE = /^\s*export\s+(?:type\s+)?(?:\*|\{[^}]*\})\s+from\s+['"][^'"]+['"]/

const sourceCache = new Map()

function readSourceLines(absPath) {
  const cached = sourceCache.get(absPath)
  if (cached !== undefined) return cached
  let lines = null
  try {
    lines = readFileSync(absPath, 'utf8').split('\n')
  } catch {
    /* non-existent file (deleted between runs) â€” keep null */
  }
  sourceCache.set(absPath, lines)
  return lines
}

/**
 * `lineSpec` is a 1-based line number; ts-prune may point to a single
 * line, but `export { foo, bar } from './x'` re-exports many names on
 * a single line. Walk backwards a few lines to find the start of a
 * multi-line `export { ... } from '...'` block.
 */
function isReExportSite(file, lineNum) {
  const lines = readSourceLines(join(ROOT, file))
  if (!lines) return false
  // ts-prune attributes `export * from './x'` re-exports to the
  // barrel path but uses the *source* line number, which can exceed
  // the barrel's own length. Only suppress if the file actually has
  // a wildcard re-export (the known cause of out-of-range entries).
  if (lineNum > lines.length) {
    return lines.some((line) => /^\s*export\s+(?:type\s+)?\*\s+from\s+['"]/.test(line))
  }
  const idx = Math.max(0, lineNum - 1)
  if (RE_EXPORT_LINE_RE.test(lines[idx])) return true
  // Walk back through the file looking for the start of a multi-line
  // `export { ... } from '...'` block that contains the target line.
  // The hops limit is intentionally generous â€” a single re-export
  // block can list 40+ symbols on separate lines.
  for (let i = idx - 1, hops = 0; i >= 0 && hops < 200; i--, hops++) {
    const line = lines[i]
    if (/^\s*export\s+(?:type\s+)?\{/.test(line)) {
      // Look forward for the matching `} from '...'`
      for (let j = i; j < lines.length && j < i + 200; j++) {
        if (/^\s*\}\s+from\s+['"][^'"]+['"]/.test(lines[j])) return j >= idx
        if (/^\s*\}/.test(lines[j])) return false
      }
      return false
    }
    if (/^\s*export\b/.test(line) || /^\s*import\b/.test(line)) break
  }
  // If the file is purely re-exports (no own definitions) and contains a
  // wildcard `export * from './x'`, the symbol may originate there with
  // this same line number â€” also noise. Only skip if the file has no own
  // export definitions (mixed files with both `export *` and own exports
  // must still report their own unused symbols).
  let hasWildcard = false
  let hasOwnExport = false
  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('//') || trimmed.startsWith('*') || trimmed.startsWith('/*')) continue
    if (/^export\s+(?:type\s+)?\*\s+from\s+['"]/.test(trimmed)) { hasWildcard = true; continue }
    if (/^export\s+(?:type\s+)?(?:\{|default).*\bfrom\s+['"]/.test(trimmed)) continue
    if (/^import\b/.test(trimmed)) continue
    if (/^export\b/.test(trimmed)) { hasOwnExport = true; break }
  }
  if (hasWildcard && !hasOwnExport) return true
  return false
}

function runTsPrune() {
  const output = execFileSync('pnpm', ['exec', 'ts-prune', '-i', TS_PRUNE_IGNORE], {
    cwd: ROOT,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe'],
  })
  return output
}

/**
 * Names ts-prune mis-parses as exports: `as const satisfies readonly Foo[]`
 * is treated as exporting names `satisfies` and `readonly`. Filter them.
 */
const TS_PRUNE_KEYWORD_FALSE_POSITIVES = new Set(['satisfies', 'readonly'])

function parseUnused(output) {
  return output
    .split('\n')
    .filter((line) => line.trim().length > 0)
    .filter((line) => !line.includes('(used in module)'))
    .filter((entry) => {
      const match = /^([^:]+):(\d+)\s+-\s+(.+)$/.exec(entry)
      if (!match) return true
      const [, file, lineStr, name] = match
      if (TS_PRUNE_KEYWORD_FALSE_POSITIVES.has(name.trim())) return false
      return !isReExportSite(file, Number(lineStr))
    })
}

const args = process.argv.slice(2)
const updateMode = args.includes('--update')

let raw
try {
  raw = runTsPrune()
} catch (err) {
  console.error('ts-prune failed:')
  console.error(err.stdout?.toString() ?? err.message)
  process.exit(2)
}

const unused = parseUnused(raw)
const count = unused.length

if (updateMode) {
  const baseline = {
    _: 'Unused-export count baseline. Updated by `node scripts/check-unused-exports.js --update`. Lower values are better; CI fails if the count grows.',
    _measured: new Date().toISOString().slice(0, 10),
    count,
  }
  writeFileSync(BASELINE_PATH, JSON.stringify(baseline, null, 2) + '\n')
  console.log(`Updated baseline: ${count} unused exports`)
  process.exit(0)
}

let baseline
try {
  baseline = JSON.parse(readFileSync(BASELINE_PATH, 'utf8'))
} catch {
  console.error(`Baseline file missing: ${BASELINE_PATH}`)
  console.error('Run `node scripts/check-unused-exports.js --update` to create it.')
  process.exit(2)
}

if (baseline === null || typeof baseline !== 'object' || Array.isArray(baseline)) {
  console.error(`Baseline ${BASELINE_PATH} is missing a numeric "count" field.`)
  process.exit(2)
}

const baselineCount = baseline.count
if (typeof baselineCount !== 'number') {
  console.error(`Baseline ${BASELINE_PATH} is missing a numeric "count" field.`)
  process.exit(2)
}

if (count > baselineCount) {
  console.error(
    `âťŚ Unused exports increased: ${count} now (baseline ${baselineCount}, +${count - baselineCount}).`
  )
  console.error('   Either remove the new unused exports, or â€” if intentionally added')
  console.error('   for an external consumer â€” re-baseline with:')
  console.error('     node scripts/check-unused-exports.js --update')
  console.error('')
  console.error('   New entries (sample, may include moved entries):')
  const baselineSet = new Set(baseline.entries ?? [])
  const newOnes = unused.filter((line) => !baselineSet.has(line))
  for (const line of newOnes.slice(0, 25)) {
    console.error(`     ${line}`)
  }
  process.exit(1)
}

if (count < baselineCount) {
  console.log(
    `Unused exports decreased: ${count} now (baseline ${baselineCount}, -${baselineCount - count}). Re-baseline to lock in the improvement:`
  )
  console.log('   node scripts/check-unused-exports.js --update')
}

console.log(`âś“ Unused exports: ${count} (baseline ${baselineCount})`)
process.exit(0)
