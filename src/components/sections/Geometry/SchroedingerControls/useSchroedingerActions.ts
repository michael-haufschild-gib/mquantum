    setDoubleWellSeparation: s.setTdseDoubleWellSeparation,
    setDoubleWellAsymmetry: s.setTdseDoubleWellAsymmetry,
    setRadialWellInner: s.setTdseRadialWellInner,
    setRadialWellOuter: s.setTdseRadialWellOuter,
    setRadialWellDepth: s.setTdseRadialWellDepth,
    setRadialWellTilt: s.setTdseRadialWellTilt,
    setDriveEnabled: s.setTdseDriveEnabled,
    setDriveWaveform: s.setTdseDriveWaveform,
    setDriveFrequency: s.setTdseDriveFrequency,
    setDriveAmplitude: s.setTdseDriveAmplitude,
    setAbsorberEnabled: s.setTdseAbsorberEnabled,
    setAbsorberWidth: s.setTdseAbsorberWidth,
    setAbsorberStrength: s.setTdseAbsorberStrength,
    setFieldView: s.setTdseFieldView,
    setAutoScale: s.setTdseAutoScale,
    setShowPotential: s.setTdseShowPotential,
    setDiagnosticsEnabled: s.setTdseDiagnosticsEnabled,
