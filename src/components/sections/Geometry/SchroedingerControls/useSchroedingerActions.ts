    setSolitonVelocity: s.setBecSolitonVelocity,
    setAutoScale: s.setBecAutoScale,
    setAbsorberEnabled: s.setBecAbsorberEnabled,
    setAbsorberWidth: s.setBecAbsorberWidth,
    setAbsorberStrength: s.setBecAbsorberStrength,
    setDiagnosticsEnabled: s.setBecDiagnosticsEnabled,
    setDiagnosticsInterval: s.setBecDiagnosticsInterval,
    setDt: s.setBecDt,
    setStepsPerFrame: s.setBecStepsPerFrame,
    setMass: s.setBecMass,
