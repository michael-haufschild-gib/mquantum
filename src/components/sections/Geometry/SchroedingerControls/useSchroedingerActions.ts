    setDiracAbsorberStrength: state.setDiracAbsorberStrength,
    setDiracDiagnosticsEnabled: state.setDiracDiagnosticsEnabled,
    setDiracDiagnosticsInterval: state.setDiracDiagnosticsInterval,
