    setTdseDoubleWellLambda: state.setTdseDoubleWellLambda,
    setTdseDoubleWellSeparation: state.setTdseDoubleWellSeparation,
    setTdseDoubleWellAsymmetry: state.setTdseDoubleWellAsymmetry,
    setTdseRadialWellInner: state.setTdseRadialWellInner,
    setTdseRadialWellOuter: state.setTdseRadialWellOuter,
    setTdseRadialWellDepth: state.setTdseRadialWellDepth,
    setTdseRadialWellTilt: state.setTdseRadialWellTilt,
