    /// Reference implementation: generic triple-loop matrix multiply
    fn reference_multiply(a: &[f64], b: &[f64], dim: usize) -> Vec<f64> {
        let mut result = vec![0.0; dim * dim];
        for i in 0..dim {
            for j in 0..dim {
                let mut sum = 0.0;
                for k in 0..dim {
                    sum += a[i * dim + k] * b[k * dim + j];
                }
                result[i * dim + j] = sum;
            }
        }
        result
    }

