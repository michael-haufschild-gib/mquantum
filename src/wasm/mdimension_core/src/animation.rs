    #[test]
    fn test_dot_product_self_equals_magnitude_squared() {
        for dim in 2..=11 {
            let v: Vec<f64> = (0..dim).map(|i| (i as f64 + 1.0) * 0.5).collect();
            let dot = dot_product(&v, &v);
            let mag = magnitude(&v);
            assert!(
                (dot - mag * mag).abs() < 1e-10,
                "dot(v,v) != ||v||^2 at dim={dim}"
            );
        }
    }

